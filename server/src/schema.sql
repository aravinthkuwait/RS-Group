-- The app connects with the postgres role (table owner) and bypasses RLS.
-- Enabling RLS with no policies blocks all access through Supabase's
-- auto-generated REST API (anon/authenticated roles) — deny by default.
DO $$
DECLARE t text;
BEGIN
  FOR t IN SELECT tablename FROM pg_tables WHERE schemaname = 'rsgroup'
  LOOP
    EXECUTE format('ALTER TABLE rsgroup.%I ENABLE ROW LEVEL SECURITY', t);
  END LOOP;
END $$;

